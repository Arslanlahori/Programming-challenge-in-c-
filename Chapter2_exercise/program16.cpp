#include <iostream>
using namespace std;

/*
       Diamond Pattern
Write a program that displays the following pattern:
    *
   ***
  *****
 *******
  *****
   ***
    *
*/
int main()
{
    cout << "    *   " << endl;
    cout << "   ***   " << endl;
    cout << "  *****   " << endl;
    cout << " *******   " << endl;
    cout << "  *****   " << endl;
    cout << "   ***   " << endl;
    cout << "    *   " << endl;
    return 0;
}